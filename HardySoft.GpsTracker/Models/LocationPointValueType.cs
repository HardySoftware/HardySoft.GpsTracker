namespace HardySoft.GpsTracker.Models
{
    /// <summary>
    /// An enumeration to have type of location point value.
    /// </summary>
    internal enum LocationPointValueType
    {
        /// <summary>
        /// Unknown point value type.
        /// </summary>
        Unknown,

        /// <summary>
        /// Point is a latitude value.
        /// </summary>
        Latitude,

        /// <summary>
        /// Point is a longitude value.
        /// </summary>
        Longitude
    }
}
